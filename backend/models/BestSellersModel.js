import mongoose from "mongoose";

const bestSellersSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
      default: 0,
    },
    description: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const BestSellers = mongoose.model("BestSellers", bestSellersSchema);

export default BestSellers;
